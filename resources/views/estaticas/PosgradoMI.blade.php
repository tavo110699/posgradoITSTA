            sistemas electrónicos y computacionales, con la finalidad de incrementar la transferencia de tecnología en los
            sectores estratégicos del país, de esta forma se crearán y desarrollar proyectos científicos, tecnológicos y de
            innovación en respuesta a las necesidades del sector público y privado de la región, bajo una filosofía de calidad
            y sustentabilidad.</p>
